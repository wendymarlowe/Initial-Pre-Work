/*Summary: A function can take parameters.  These act as placeholders for the
values, or arguments, that will be passed into the function.  The function can
then use the values(arguments) passed in to do something.
*/

// Example
function ourFunctionWithArgs(a, b) {
  console.log(a - b);
}
ourFunctionWithArgs(10, 5); // Outputs 5

// Only change code below this line.
function functionWithArgs(num1, num2) {
  console.log(num1 + num2);
}

functionWithArgs(16, 34);
