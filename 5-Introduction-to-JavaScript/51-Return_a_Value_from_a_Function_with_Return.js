/*Summary: Functions can take an value passed in (argument) and return a value.
You can use the return statement to return a value from a function.  Here we pass
a number, perform a calculation and return the answer.*/

// Example
function minusSeven(num) {
  return num - 7;
}

// Only change code below this line
function timesFive(num) {
  return num * 5;
}


console.log(minusSeven(10));

console.log(timesFive(15));
